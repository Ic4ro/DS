    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int linha = tbLista.getSelectedRow();// SELECIONA A LINHA

        //CASO NÃO SELECIONE A LINHA 
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma pessoa para remover.");
            return;

        }

        ArrayList<Animais> Lista = controller.listar();
        Lista.remove(linha);
        atualizarTabela();
        LimparCampos();
        JOptionPane.showMessageDialog(null, "Animal removido com sucesso");
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void atualizarTabela() {
        modeloTabela.setRowCount(0); // Limpa
        for (Animais a : controller.listar()) {
            modeloTabela.addRow(new Object[]{a.getNome(), a.getEspecie(), a.getIdade(), a.getId()});

        }
    }

    private void LimparCampos() {
        txtNome.setText("");
        txtEspecie.setText("");
        txtBuscar.setText("");
        txtIdade.setText("");
        tbLista.clearSelection();
    }

    private void filtrarTabela() {
        String termo = txtBuscar.getText().trim().toLowerCase();
        modeloTabela.setRowCount(0);

        ArrayList<Animais> listaOriginal = controller.listar();

        for (Animais a : listaOriginal) {
            if (a.getNome().toLowerCase().contains(termo)) {
                modeloTabela.addRow(new Object[]{a.getNome(), a.getEspecie(), a.getIdade(), a.getId()});
            }
        }

        if (termo.isEmpty()) {
            atualizarTabela();
        }
    }


