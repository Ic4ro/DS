    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String nome = txtNome.getText();
        String especie = txtEspecie.getText();
        String idadeTexto = txtIdade.getText();

        if (nome.isEmpty() || especie.isEmpty() || idadeTexto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            return;
        }

        try {
            int idade = Integer.parseInt(idadeTexto);
            controller.cadastrar(nome, especie, idade);

            // Atualiza a tabela
            modeloTabela.setRowCount(0); //Limpa
            ArrayList<Animais> lista = controller.listar();
            for (Animais a : lista) {
                modeloTabela.addRow(new Object[]{a.getNome(), a.getEspecie(), a.getIdade()});
            }

            txtNome.setText("");
            txtEspecie.setText("");
            txtIdade.setText("");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Idade Inválida");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linha = tbLista.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um animal na tabela para alterar.");
            return;
        }

        String novoNome = txtNome.getText();
        String novaEspecie = txtEspecie.getText();
        String novaIdadeTexto = txtIdade.getText();

        if (novoNome.isEmpty() || novaEspecie.isEmpty() || novaIdadeTexto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preecha os campos!");
            return;
        }

        try {
            int novaIdade = Integer.parseInt(novaIdadeTexto);

            // Atualiza a lista (em memoória)
            ArrayList<Animais> lista = controller.listar();
            Animais a = lista.get(linha);
            a.setNome(novoNome);
            a.setEspecie(novaEspecie);
            a.setIdade(novaIdade);

            // Atualiza a tabela
            modeloTabela.setValueAt(novoNome, linha, 0);
            modeloTabela.setValueAt(novaEspecie, linha, 1);
            modeloTabela.setValueAt(novaIdade, linha, 2);

            JOptionPane.showMessageDialog(null, "Animal atualizado com sucesso!");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Idade inválida");

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

