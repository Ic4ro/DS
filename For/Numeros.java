public class Numeros {
    public static void main(String[] args) {
        
        
        for(int x = 10; x <= 20; x++) {
            System.out.println("\nNúmeros de 10 a 20: " + x);
        }

        
        for(int i = 20; i >= 10; i--) {
            System.out.println("\nNúmeros de 20 a 10: " + i);
        }
    }
}
